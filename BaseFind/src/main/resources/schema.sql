DROP TABLE IF EXISTS baby;

CREATE TABLE baby (
  BabyId    CHAR (20) NOT NULL UNIQUE PRIMARY KEY,
  BabyName VARCHAR (50) NOT NULL ,
  BabyClass VARCHAR (20) DEFAULT NULL,
  BabyFindTime DATETIME NOT NULl,
  BabyLookLike VARCHAR (255),
  BabyLong DOUBLE DEFAULT NULL ,
  BabyLat DOUBLE DEFAULT NULL ,
  BabyPassSecret VARCHAR (50) DEFAULT NULL ,
  BabySecretKey VARCHAR (50) DEFAULT NULL ,
  BabyUpdateTime DATETIME NOT NULL,
  BabyFindMark INTEGER DEFAULT NULL ,
  BabyMark INTEGER DEFAULT NULL ,
  BabySlead INTEGER DEFAULT NULL ,
  BabyFindPhone VARCHAR (11) NOT NULL
);